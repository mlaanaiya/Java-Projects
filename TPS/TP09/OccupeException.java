public class OccupeException extends Exception {

	public OccupeException(String message) {
		super(message);
	}

	public OccupeException() {
		super();
	}

	public OccupeException(Throwable cause) {
		super(cause);
	}

	public OccupeException(String message, Throwable cause) {
		super(message, cause);
	}

}
